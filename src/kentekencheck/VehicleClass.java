package kentekencheck;

public class VehicleClass {
    String kenteken;
    String merk;
    String model;
    String vervaldatum;

    VehicleClass( String kenteken, String merk, String model, String vervaldatum ) {
        this.kenteken = kenteken;
        this.merk = merk;
        this.model = model;
        this.vervaldatum = vervaldatum;
    }
}
